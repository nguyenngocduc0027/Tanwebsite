<script>
    var swiper_brand = null;

    function initSwiperBrand() {
        swiper_brand = new Swiper('.swiper_brands', {
            slidesPerView: 6,
            spaceBetween: 20,
            watchOverflow: true,
            slidesPerGroup: 1,
            navigation: {
                nextEl: '.swiper_brands .swiper-button-next',
                prevEl: '.swiper_brands .swiper-button-prev',
            },
            breakpoints: {
                640: {
                    slidesPerView: 4,
                    spaceBetween: 14
                },
                768: {
                    slidesPerView: 5,
                    spaceBetween: 14
                },
                992: {
                    slidesPerView: 6,
                    spaceBetween: 20
                },
                1024: {
                    slidesPerView: 6,
                    spaceBetween: 20
                },
                1200: {
                    slidesPerView: 8,
                    spaceBetween: 20
                }
            }
        });
    }

    function destroySwiperBrand() {
        if (swiper_brand) {
            swiper_brand.destroy(true, true);
            swiper_brand = null;
        }
    }

    function toggleSwiperBrand() {
        if ($(window).width() <= 767 && swiper_brand) {
            destroySwiperBrand();
        } else if ($(window).width() > 767 && !swiper_brand) {
            initSwiperBrand();
        }
    }
    toggleSwiperBrand();
    $(window).on('resize', function() {
        toggleSwiperBrand();
    });
</script>
